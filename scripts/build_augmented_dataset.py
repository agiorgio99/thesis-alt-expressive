# ── Per-utterance worker (called in subprocess) ───────────────────────────────

def _process_utterance(args_tuple):
    """
    Process one control WAV → list of (wav_path, json_path) output pairs.
    Returns (n_ok, n_err, list_of_skipped_messages).
    """
    ctrl_wav_str, out_tech_dir_str, cfg_key, dry_run = args_tuple
    ctrl_wav      = Path(ctrl_wav_str)
    out_tech_dir  = Path(out_tech_dir_str)
    cfg           = TECHNIQUE_CONFIG[cfg_key]
    params_list   = cfg["params"]
    augment_fn    = cfg["augment_fn"]

    ctrl_json = ctrl_wav.with_suffix(".json")
    if not ctrl_json.exists():
        return 0, 0, [f"SKIP (no json): {ctrl_wav}"]

    # Check if all variants already exist (resumable run)
    stem = ctrl_wav.stem
    all_exist = all(
        (out_tech_dir / f"{stem}_v{k}.wav").exists()
        for k in range(len(params_list))
    )
    if all_exist:
        return len(params_list), 0, []

    try:
        wav, meta = load_utterance(ctrl_wav, ctrl_json)
        f0, sp, ap = world_decompose(wav)
    except Exception as e:
        return 0, 1, [f"ERR load {ctrl_wav}: {e}"]

    n_ok, n_err = 0, 0
    msgs = []
    for k, params in enumerate(params_list):
        out_wav  = out_tech_dir / f"{stem}_v{k}.wav"
        out_json = out_tech_dir / f"{stem}_v{k}.json"
        if out_wav.exists():
            n_ok += 1
            continue
        if dry_run:
            msgs.append(f"[DRY] {out_wav}")
            n_ok += 1
            continue
        try:
            wav_aug, meta_aug = augment_fn(f0, sp, ap, meta, params)
            sf.write(out_wav, wav_aug, SR)
            with open(out_json, "w") as fh:
                json.dump(meta_aug, fh, indent=2)
            n_ok += 1
        except Exception as e:
            n_err += 1
            msgs.append(f"ERR aug {ctrl_wav} v{k}: {e}\n{traceback.format_exc()}")
    return n_ok, n_err, msgs

# ── Link / copy helper ────────────────────────────────────────────────────────

def _link_or_copy(src: Path, dst: Path, do_copy: bool, dry_run: bool):
    if dst.exists() or dst.is_symlink():
        return
    if dry_run:
        action = "COPY" if do_copy else "LINK"
        log.info("[DRY] %s %s → %s", action, src, dst)
        return
    if do_copy:
        shutil.copytree(src, dst)
    else:
        dst.symlink_to(src.resolve())

# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument(
        "--src", default="data/GTSinger/English",
        help="Original GTSinger English directory (default: data/GTSinger/English)",
    )
    parser.add_argument(
        "--dst", default="data/GTSinger_Augmented/English",
        help="Output directory (default: data/GTSinger_Augmented/English)",
    )
    parser.add_argument(
        "--techniques", nargs="+", default=list(TECHNIQUE_CONFIG.keys()),
        choices=list(TECHNIQUE_CONFIG.keys()),
        metavar="TECHNIQUE",
        help="Which techniques to augment (default: all). "
             f"Choices: {list(TECHNIQUE_CONFIG.keys())}",
    )
    parser.add_argument(
        "--copy", action="store_true",
        help="Copy Control_Group / Paired_Speech_Group instead of symlinking",
    )
    parser.add_argument(
        "--dry-run", action="store_true",
        help="Print what would be done without writing any files",
    )
    parser.add_argument(
        "--workers", type=int, default=1,
        help="Parallel worker processes (default: 1). "
             "Use 4+ on multi-core machines for ~4× speedup.",
    )
    args = parser.parse_args()

    repo = Path(__file__).parent.parent
    src  = (repo / args.src) if not Path(args.src).is_absolute() else Path(args.src)
    dst  = (repo / args.dst) if not Path(args.dst).is_absolute() else Path(args.dst)

    if not src.exists():
        log.error("Source directory not found: %s", src)
        sys.exit(1)

    log.info("Source : %s", src)
    log.info("Output : %s", dst)
    log.info("Techniques : %s", args.techniques)
    log.info("Workers    : %d", args.workers)
    if args.dry_run:
        log.info("DRY RUN — no files will be written")

    # ── Collect tasks ─────────────────────────────────────────────────────────
    # Each task = (ctrl_wav_path, out_tech_dir, technique_name, dry_run)
    tasks = []

    for singer_dir in sorted(src.iterdir()):
        if not singer_dir.is_dir():
            continue
        for tech_dir in sorted(singer_dir.iterdir()):
            if not tech_dir.is_dir():
                continue
            if tech_dir.name not in args.techniques:
                continue
            cfg = TECHNIQUE_CONFIG[tech_dir.name]

            for song_dir in sorted(tech_dir.iterdir()):
                if not song_dir.is_dir():
                    continue
                ctrl_dir = song_dir / "Control_Group"
                if not ctrl_dir.exists():
                    continue

                out_song = dst / singer_dir.name / tech_dir.name / song_dir.name
                if not args.dry_run:
                    out_song.mkdir(parents=True, exist_ok=True)

                # Symlink / copy static groups
                for grp in ("Control_Group", "Paired_Speech_Group"):
                    src_grp = song_dir / grp
                    dst_grp = out_song / grp
                    if src_grp.exists():
                        _link_or_copy(src_grp, dst_grp, args.copy, args.dry_run)

                # Create technique output dir
                out_tech = out_song / cfg["group_name"]
                if not args.dry_run:
                    out_tech.mkdir(exist_ok=True)

                for ctrl_wav in sorted(ctrl_dir.glob("*.wav")):
                    tasks.append((
                        str(ctrl_wav),
                        str(out_tech),
                        tech_dir.name,
                        args.dry_run,
                    ))

    n_tasks = len(tasks)
    log.info("Total control utterances to process: %d", n_tasks)
    if n_tasks == 0:
        log.warning("Nothing to do — check --src path and --techniques filter.")
        return

    # ── Run augmentation ──────────────────────────────────────────────────────
    total_ok  = 0
    total_err = 0

    if args.workers > 1:
        with ProcessPoolExecutor(max_workers=args.workers) as ex:
            futures = {ex.submit(_process_utterance, t): t for t in tasks}
            for fut in tqdm(as_completed(futures), total=n_tasks, desc="augmenting"):
                n_ok, n_err, msgs = fut.result()
                total_ok  += n_ok
                total_err += n_err
                for m in msgs:
                    (log.info if m.startswith("[DRY]") else log.warning)(m)
    else:
        for task in tqdm(tasks, desc="augmenting"):
            n_ok, n_err, msgs = _process_utterance(task)
            total_ok  += n_ok
            total_err += n_err
            for m in msgs:
                (log.info if m.startswith("[DRY]") else log.warning)(m)

    log.info("Done. Variants written: %d | Errors: %d", total_ok, total_err)
    if total_err:
        log.warning("%d errors — check log above for details.", total_err)


if __name__ == "__main__":
    main()
